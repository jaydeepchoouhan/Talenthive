function interpolate(template, values = {}) {
  return Object.entries(values).reduce(
    (output, [key, value]) => output.replaceAll(`{${key}}`, String(value)),
    template
  );
}

export function isSupportedLanguage(language) {
  return LANGUAGE_OPTIONS.some((item) => item.code === language);
}

export function getLanguageLocale(language) {
  return LANGUAGE_OPTIONS.find((item) => item.code === language)?.locale || 'en-US';
}

export function translateText(language, key, values) {
  const selectedLanguage = translations[language] || translations[DEFAULT_LANGUAGE];
  const template = selectedLanguage[key] || translations[DEFAULT_LANGUAGE][key] || key;
  return interpolate(template, values);
}

export function translateApiMessage(message, translate) {
  const key = apiMessageKeyMap[message];
  return key ? translate(key) : message;
}
